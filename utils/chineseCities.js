function haversineKm(lat1, lng1, lat2, lng2) {
  const R = 6371
  const dLat = (lat2 - lat1) * Math.PI / 180
  const dLng = (lng2 - lng1) * Math.PI / 180
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2)
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
}

/**
 * @param {number} lat
 * @param {number} lng
 * @returns {{ name: string, distanceKm: number }}
 */
function findNearestCity(lat, lng) {
  const la = Number(lat)
  const ln = Number(lng)
  if (!Number.isFinite(la) || !Number.isFinite(ln)) {
    return { name: '深圳', distanceKm: Infinity }
  }
  let bestName = '深圳'
  let bestDist = Infinity
  for (let i = 0; i < CITY_COORD_ENTRIES.length; i++) {
    const entry = CITY_COORD_ENTRIES[i]
    const dist = haversineKm(la, ln, entry[1], entry[2])
    if (dist < bestDist) {
      bestDist = dist
      bestName = entry[0]
    }
  }
  return { name: bestName, distanceKm: bestDist }
}

/**
 * @param {string} keyword
 * @param {number} [limit]
 */
function filterCities(keyword, limit) {
  const k = String(keyword || '').trim()
  const max = limit || 300
  if (!k) return SEARCH_CITIES.slice(0, max)
  const starts = []
  const contains = []
  for (let i = 0; i < SEARCH_CITIES.length; i++) {
    const name = SEARCH_CITIES[i]
    if (name.indexOf(k) === 0) starts.push(name)
    else if (name.indexOf(k) !== -1) contains.push(name)
    if (starts.length + contains.length >= max) break
  }
  return starts.concat(contains).slice(0, max)
}

